%% P_matrix.m
function P = P_matrix(x, y)
    P = [1, -x, -y, -(x^2)/2, -(x^3)/6, -((x^2*y)/2 + y^4/12), -((x^3*y)/6), -(y^2)/2, -((y^2*x)/2 + x^4/12), -(y^3)/6, -((y^3*x)/6), -((x*y)/2);
         0,  1,  0,       x,       x^2/2,               x*y,              (x^2*y)/2,       0,               (y^2)/2 + x^3/3,   0,         y^3/6,      y/2;
         0,  0,  1,       0,           0,      (y^3/3 + x^2/2),      x^3/6,              y,               x*y,            y^2/2,     (y^2*x)/2,   x/2];
end